// Given two numeric values, 72 and 104,
// write a program to print out the corresponding two characters.
#include <stdio.h>
int main()
{
    int num1, num2;
    num1 = 72;
    num2 = 104;
    printf("The ANSI number 72 corresponds to: %c\n", num1);
    printf("The ANSI number 104 corresponds to: %c\n", num2);
    return 0;
}